function translate(lang, key) {
  if (exact[lang][key]) {
    return exact[lang][key];
  }

  const validationTranslation = validation(lang, key);

  if (validationTranslation) {
    return validationTranslation;
  }

  let match = key.match(/^Enter a valid (.+)\.$/);

  if (match) {
    if (lang === "ar") {
      return match[1] === "email address"
        ? "أدخل عنوان بريد إلكتروني صالحا."
        : `أدخل ${field(lang, match[1])} صالحا.`;
    }

    if (lang === "de") {
      return match[1] === "email address"
        ? "Geben Sie eine gültige E-Mail-Adresse ein."
        : `Geben Sie eine gültige Option für ${field(lang, match[1])} ein.`;
    }

    return match[1] === "email address"
      ? "Introduce una dirección de correo válida."
      : `Introduce ${field(lang, match[1]).toLowerCase()} válido.`;
  }

  match = key.match(/^Choose a supported (.+)\.$/);

  if (match) {
    return lang === "ar"
      ? `اختر ${field(lang, match[1])} مدعوما.`
      : lang === "de"
        ? `Wählen Sie eine unterstützte Option für ${field(lang, match[1])}.`
        : `Elige ${field(lang, match[1]).toLowerCase()} compatible.`;
  }

  match = key.match(/^Choose a valid (.+)\.$/);

  if (match) {
    return lang === "ar"
      ? `اختر ${field(lang, match[1])} صالحا.`
      : lang === "de"
        ? `Wählen Sie eine gültige Option für ${field(lang, match[1])}.`
        : `Elige ${field(lang, match[1]).toLowerCase()} válido.`;
  }

  return key;
}

for (const language of languages) {
  const patches = {
    "common.json": commonPatches[language],
    ...catalogPatches[language],
  };

  for (const [filename, patch] of Object.entries(patches)) {
    const catalogPath = path.join(localesRoot, language, filename);
    const catalog = JSON.parse(readFileSync(catalogPath, "utf8"));

    deepMerge(catalog, patch);
    writeFileSync(catalogPath, `${JSON.stringify(catalog, null, 2)}\n`, "utf8");
  }

  const legacy = {};

  for (const key of Object.keys(enLegacy)) {
    legacy[key] = translate(language, key);
  }

  writeFileSync(
    path.join(localesRoot, language, "legacy.json"),
    `${JSON.stringify(legacy, null, 2)}\n`,
    "utf8",
  );
}
